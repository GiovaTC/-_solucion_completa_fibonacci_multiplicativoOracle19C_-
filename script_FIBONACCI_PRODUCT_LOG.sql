CREATE TABLE FIBONACCI_PRODUCT_LOG (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TOTAL_NUMBERS NUMBER,
    RESULT_VALUE NUMBER,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE PROCEDURE INSERT_FIBONACCI_PRODUCT (
    p_total_numbers IN NUMBER,
    p_result_value  IN NUMBER
) AS
BEGIN
    INSERT INTO FIBONACCI_PRODUCT_LOG (
        TOTAL_NUMBERS,
        RESULT_VALUE
    ) VALUES (
        p_total_numbers,
        p_result_value
    );

    COMMIT;
END;
